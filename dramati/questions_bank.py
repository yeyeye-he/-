def get_questions(group: str = "main") -> list[dict]:
    """group:
    - main：Q01–Q32
    - extra：Q33–Q40
    - content：Q41–Q52（戏剧内容加餐）
    - main_extra：32+8
    - main_content：32+12
    - all：32+8+12
    - shakespeare：莎士比亚专题卷（独立题组，见 `_questions_shakespeare`）
    """
    main = _questions_main()
    extra = _questions_extra()
    content = _questions_content()
    if group == "main":
        return main
    if group == "extra":
        return extra
    if group == "content":
        return content
    if group == "main_extra":
        return main + extra
    if group == "main_content":
        return main + content
    if group == "all":
        return main + extra + content
    if group == "shakespeare":
        return _questions_shakespeare()
    raise ValueError(
        "group must be main | extra | content | main_extra | main_content | all | shakespeare"
    )


