def sub_maxima(questions: list[dict]) -> dict[str, int]:
    keys = [f"sub{i}" for i in range(1, 16)]
    m: dict[str, int] = {k: 0 for k in keys}
    for q in questions:
        for k in keys:
            m[k] += max(o["w"].get(k, 0) for o in q["options"])
    return m


def compute_scores(
    questions: list[dict],
    answers: list[int],
) -> dict:
    """
    answers[i] = 0..3 表示第 i 题所选选项下标。
    返回主类型 code、各轴原始分、子维、匹配度等。
    """
    if len(answers) != len(questions):
        raise ValueError("题目数量与答案数量不一致")

    axes = {"I": 0, "B": 0, "C": 0, "E": 0, "S": 0, "Q": 0, "T": 0, "G": 0}
    sub = {f"sub{i}": 0 for i in range(1, 16)}
    sub8_ear = 0
    sub8_eye = 0

    max_margin = {"IB": 0, "CE": 0, "SQ": 0, "TG": 0}

    for q in questions:
        opts = q["options"]
        for o in opts:
            w = o["w"]
            max_margin["IB"] += max(w.get("I", 0), w.get("B", 0))
            max_margin["CE"] += max(w.get("C", 0), w.get("E", 0))
            max_margin["SQ"] += max(w.get("S", 0), w.get("Q", 0))
            max_margin["TG"] += max(w.get("T", 0), w.get("G", 0))

    for i, q in enumerate(questions):
        idx = answers[i]
        w = q["options"][idx]["w"]
        for k in axes:
            axes[k] += w.get(k, 0)
        for k in sub:
            sub[k] += w.get(k, 0)
        sub8_ear += w.get("sub8_ear", 0)
        sub8_eye += w.get("sub8_eye", 0)

    def pick(first: str, second: str) -> str:
        a, b = axes[first], axes[second]
        if a >= b:
            return first
        return second

    letters = [
        pick("I", "B"),
        pick("C", "E"),
        pick("S", "Q"),
        pick("T", "G"),
    ]
    code = "".join(letters)

    margins = [
        abs(axes["I"] - axes["B"]) / max(max_margin["IB"], 1),
        abs(axes["C"] - axes["E"]) / max(max_margin["CE"], 1),
        abs(axes["S"] - axes["Q"]) / max(max_margin["SQ"], 1),
        abs(axes["T"] - axes["G"]) / max(max_margin["TG"], 1),
    ]
    avg = sum(margins) / 4
    match_pct = int(round(70 + min(1.0, avg) * 29))

    sub_out = {k: sub[k] for k in sub}
    mx = sub_maxima(questions)
    sub_percent = {}
    for k in sub_out:
        denom = max(mx.get(k, 0), 1)
        sub_percent[k] = int(round(100 * min(1.0, sub_out[k] / denom)))

    ear_eye = sub8_eye - sub8_ear
    denom = sub8_ear + sub8_eye + 1
    sub8_display = int(round(50 + 50 * (ear_eye / denom)))
    sub8_display = min(100, max(0, sub8_display))

    return {
        "code": code,
        "axes": axes,
        "sub": sub_out,
        "sub_percent": sub_percent,
        "sub8_ear": sub8_ear,
        "sub8_eye": sub8_eye,
        "sub8_index": sub8_display,
        "match_percent": match_pct,
        "margins": {"IB": margins[0], "CE": margins[1], "SQ": margins[2], "TG": margins[3]},
    }


def axes_from_code(code: str) -> list[dict]:
    """由四字母结果生成通俗「人格侧写」标签，供前端展示。"""
    meta = [
        ("I", "B", "入戏派", "清醒派", "情绪跟着戏走", "脑子里常跑「导演想干啥」"),
        ("C", "E", "经典胃", "新潮胃", "老本子长剧也能啃", "当代怪东西更来劲"),
        ("S", "Q", "聚聊型", "独食型", "中场散场都爱唠", "一个人看戏更自在"),
        ("T", "G", "台词党", "整体控", "动大刀子改词会心痛", "声光电对了你就买账"),
    ]
    if len(code) != 4:
        return []
    out: list[dict] = []
    for i, ch in enumerate(code):
        pole_a, pole_b, ta, tb, ha, hb = meta[i]
        if ch == pole_a:
            out.append({"code": ch, "title": ta, "hint": ha})
        elif ch == pole_b:
            out.append({"code": ch, "title": tb, "hint": hb})
        else:
            out.append({"code": ch, "title": "?", "hint": ""})
    return out


